    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        // TODO add your handling code here:
        String username = usernameField.getText();
        String password = pwField.getText();
        
        boolean isSuccess = userService.userAuthentication(username, password);
        boolean isAdmin = ("admin".equals(username) && password.equals("admin123"));
        if(isAdmin) {
            try {
                UserSession userSession = UserSession.getInstance();
                userSession.setUserInfo(username, "ADMIN");
                new UserManagementView().setVisible(true);
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(LoginFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.dispose();
            
        }
        else if (isSuccess) {

            try {
                User user = userService.getUserByUsername(username);
                UserSession userSession = UserSession.getInstance();
                userSession.setUserInfo(username, user.getRole());
                new UserManagementView().setVisible(true);
            } catch (SQLException | ClassNotFoundException ex) {
                Logger.getLogger(LoginFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.dispose();
        } else
            JOptionPane.showMessageDialog(LoginFrame.this, "Wrong username or password", "Delete Student", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_loginBtnActionPerformed

